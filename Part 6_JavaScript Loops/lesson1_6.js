// 1. What are Loops
// - Loops allow us to repeat code
//      + Print "Hello World!" 10 times
//      + Sum all numbers in an array
// - There are multiple types of Loops:
//      + For Loop
//      + While Loop
//      + For...Of Loop
//      + For...In Loop

// 2. For Loops Syntax
// - Syntax: 
// for(
//     [initialExpression];
//     [condition];
//     [incrementExpression];
// ) {....code being repeated in here...}

// Example (Our First For Loop):
for (let i = 0; i <= 10; i++) {
    console.log(i);  
}
// This For Loop will print out from 0 to 10

