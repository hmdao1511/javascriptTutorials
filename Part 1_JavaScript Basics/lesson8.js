// 1. Naming variables
// When naming a variable (for var and let), remember that:
// - A variable name cannot have spaces between the words
// - A variable name cannot start with a number (though we can have numbers in
// the name of the variable, but just not at the start)
// - A variable name must be in camelCase (snake_case is also valid, but
// it's not commonly used specifically in JavaScript)
// - A variable name must have a meaning to it, so don't make 
// meaningless variable names!