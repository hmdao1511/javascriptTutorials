// Comparision operators:
// >         greater than
// <         less than
// >=        greater than or equal to 
// <=        less than or equal to 
// ==        equality
// !=        not equal
// ===       strict equality
// !==       strict non-equality

// Though it's uncommon, you can compare Strings, too. Just be careful, the 
// characters will be converted to their respective Unicode values, so that's why
// you can compare Strings!