// 1. Syntax
// It's a 2-step process:
//      DEFINE THE FUNCTION -> RUN THE FUNCTION

// - Step 1: Define
// It should follow this form:
// function funcName() {
//     do something   
// }
// (Later on we will discover that we can have a parameter in a function, but for now
// just don't worry about it)

// Example:
// This function below is used for greeting :)
function greetings(){
    console.log(`Hello! It's very nice to meet you!`);
}

// - Step 2: Run 
// Now that the function exists, you need to call it out in order to run the code
// inside the function, like this:
// funcName()    // run once

// function()    // run again!

// Example (let's run the function that we've made ):
greetings();
greetings();
greetings();
greetings();
greetings();

