// Lesson 9: Traversing Parent/Child/Sibling of an element

// 1. For finding the parent element of an element, use the parentElement attribute
// Example:
h1.parentElement

// 2. For knowing how many child elements are there in an element, use the 
// childElementCount attribute
// Example:
h1.childElementCount // Returns an integer that indicates the number of child elements

// 3. For finding a list of child elements in an element, use the children attribute
// Example:
h1.children // Returns an HTMLCollection of the child elements in that element

// 4. To find the previous sibling element of an element, use the previousElementSibling
// attribute
// Example:
h1.previousElementSibling

// 5. To find the next sibling element of an element, use the nextElementSibling attribute
// Example:
h1.nextElementSibling